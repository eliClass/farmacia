    private void reportar() {
        try {
            limpiar_tabla_formulario();

            DefaultTableModel tabla_temporal_composion = (DefaultTableModel) tabla_reporte_composicion.getModel();
            ComposicionBD oComposicionBD = new ComposicionBD();

            List<Composicion> lista_composicion = oComposicionBD.reportarComposicion();
            int cant = lista_composicion.size();

            for (int i = 0; i < cant; i++) {
                int idcomposicion = lista_composicion.get(i).getIdcomposicion();
                String nombre_composicion = lista_composicion.get(i).getCoNombre();
                String serie_producto = lista_composicion.get(i).getpSerie();

                Object[] data = {idcomposicion, nombre_composicion, serie_producto};
                tabla_temporal_composion.addRow(data);

            }
            tabla_reporte_composicion.setModel(tabla_temporal_composion);
        } catch (Exception e) {
        }
    }

