    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if (txtSerie.getText().length() > 0) {
            if (txtComposicion.getText().length() > 0) {
              
                       Composicion oComposicion = new Composicion();
                       ComposicionBD oComposicionBD = new ComposicionBD();

                        oComposicion.setCoNombre(txtComposicion.getText().toUpperCase().trim());
                        oComposicion.setpSerie(txtSerie.getText());
                        
                        boolean rpta = oComposicionBD.registrarComposicion(oComposicion);

                        if (rpta) {
                            exito("Se registro con exito");
                            reportar();
                            limpiar();
                      
                        } else {
                            error("Tienes problemas al registrar");
                        }

            } else {
                error("Por favor Ingrese una composicion");
                txtComposicion.requestFocus();
            }

        } else {
            error("por favor, tienes que agregar un serie ");
            txtSerie.requestFocus();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (txtSerie.getText().length() > 0) {
            ProductoBD oProductoBD = new ProductoBD();

            DefaultTableModel tabla_temporal;
            String serie = txtSerie.getText();
            tabla_temporal = oProductoBD.buscarProducto(serie);

            String producto = tabla_temporal.getValueAt(0, 1).toString();

            txtProducto.setText(producto);

        } else {
            advertencia("Ingrese la serie del producto");
            txtSerie.requestFocus();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila_selecionada = tabla_reporte_composicion.getSelectedRow();
        if (fila_selecionada > -1) {
            int aviso= JOptionPane.showConfirmDialog(rootPane, "ESTAS DE ELIMINAR");
            
            if (aviso == 0) {
                int idcomposicion=Integer.parseInt(tabla_reporte_composicion.getValueAt(fila_selecionada, 0).toString());
                ComposicionBD oComposicionBD = new ComposicionBD();
                boolean rpta= oComposicionBD.eliminarComposicion(idcomposicion);
                if (rpta) {
                    exito("SE ELIMINO CON EXITO");
                    reportar();
                    limpiar();
                } else {
                    error("TIENES PROBLEMAS AL ELIMINAR");
                }
            }
        }else{
            advertencia("TIENES QUE SELECIONAR UNA COMPOSICION PARA ELIMINAR");
            
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

